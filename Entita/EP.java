package Entita;

public class EP {
	String name;
	String id_EP;
	
	public EP(String name, String EP) {
		setName(name);
		setId_EP(EP);
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId_EP() {
		return id_EP;
	}
	public void setId_EP(String id_EP) {
		this.id_EP = id_EP;
	}
}
